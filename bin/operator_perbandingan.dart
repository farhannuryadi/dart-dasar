void main(){

  //operator sama dengan ==
  print(10 == 10);
  print('farhan' == 'farhan');
  print('');

  //operatot tidak sama dengan !=
  print(10 != 10);
  print('farhan' != 'farhan');
  print('');

  //operator lebih dari >
  print(10 > 10);
  print('farhan'.length > 'farhan'.length);
  print('');

  //operator kurang dari <
  print(10 < 10);
  print('farhan'.length < 'farhan'.length);
  print('');

  //operator lebih dari sama dengan >=
  print(10 >= 10);
  print('farhan'.length >= 'farhan'.length);
  print('');

  //operator kurang dari sama dengan <=
  print(10 <= 10);
  print('farhan'.length <= 'farhan'.length);
  print('');

}